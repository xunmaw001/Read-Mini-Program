				shuchengfenleiIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   tushumingcheng : false,
                                   shuchengfenlei : false,
                                   jianjie : false,
                                   zuozhexingming : false,
                                   chubanshe : false,
                                   shangjiariqi : false,
                                   tushuneirong : false,
                                   fengmian : false,
